package jinshen.bean;

public class yardManage {
	private int yardid;
	private String yardname;
	private Double yardvolume;
	private String yardmanager;
	private String managephone;
	private String remarks;
	private String section;
	public int getYardid() {
		return yardid;
	}
	public void setYardid(int yardid) {
		this.yardid = yardid;
	}
	public String getYardname() {
		return yardname;
	}
	public void setYardname(String yardname) {
		this.yardname = yardname;
	}
	public Double getYardvolume() {
		return yardvolume;
	}
	public void setYardvolume(Double yardvolume) {
		this.yardvolume = yardvolume;
	}
	public String getYardmanager() {
		return yardmanager;
	}
	public void setYardmanager(String yardmanager) {
		this.yardmanager = yardmanager;
	}
	public String getManagephone() {
		return managephone;
	}
	public void setManagephone(String managephone) {
		this.managephone = managephone;
	}
	public String getRemarks() {
		return remarks;
	}
	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}
	public String getSection() {
		return section;
	}
	public void setSection(String section) {
		this.section = section;
	}
	

}
